public class ve_hinh_dong_ho_cat {
    public static void main(String[] args) {
        int n = 8;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.println("*");
            }
            System.out.println(" ");
        }
        System.out.println("--");
    }
}
